     * @var array
     */
    protected $fillable = [
        'name',
        'username',
        'email',
        'profile_picture',
        'is_admin',
    ];

    /**
     * The attributes that should be hidden for arrays.
     *
     * @var array
     */
    protected $hidden = [
        'remember_token',
    ];
}
